"""
Модуль с перечислением возможных результатов хода в игре "Виселица"
"""
from enum import Enum


class GuessResult(Enum):
    """
    Перечисление результатов попытки угадать букву.

    Возвращается методом GallowEngine.guess() для информирования
    пользовательского интерфейса о результате хода.

    Attributes:
        CORRECT: Буква угадана правильно, игра продолжается.
        INCORRECT: Буква не угадана, попытка потрачена.
        ALREADY_USED: Буква уже была введена ранее.
        INVALID: Введен некорректный символ (не буква или больше одного символа).
        WIN: Игрок угадал последнюю букву и выиграл.
        LOSE: Игрок исчерпал все попытки и проиграл.
    """
    CORRECT = 1
    INCORRECT = 2
    ALREADY_USED = 3
    INVALID = 4
    WIN = 5
    LOSE = 6
